#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		k;
	int		j;

	i = 0;
	k = 0;
	j = 1;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t' || s[i] == '\v'
			|| s[i] == '\r' || s[i] == '\f' || s[i] == '0')
		i++;
	if ((s[i] == '-' || s[i] == '+') && ft_isdigit(s[i + 1]))
	{
		j = ',' - s[i];
		i++;
	}
	while (s[i] != '\0')
	{
		if (!ft_isdigit(s[i]))
			return (k * j);
		k = k * 10 + s[i] - '0';
		i++;
	}
	return (k * j);
}
