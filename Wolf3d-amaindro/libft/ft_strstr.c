#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (s2[i] == '\0')
		return ((char*)s1);
	while (s1[i] != '\0')
	{
		if (s2[j] == '\0')
			return ((char*)&s1[i]);
		if (s1[i + j] == s2[j])
			j++;
		else
		{
			j = 0;
			i++;
		}
	}
	return (0);
}
